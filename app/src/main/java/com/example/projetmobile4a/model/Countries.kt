package com.example.projetmobile4a.model

class Countries {
    private val name: String? = null
    private val capital: String? = null
    private val region: String? = null

    fun Countries() {}

    fun getName(): String? {
        return name
    }

    fun getCapital(): String? {
        return capital
    }

    fun getRegion(): String? {
        return region
    }
}